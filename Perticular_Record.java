    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String sid = studid.getSelectedItem().toString();
            pst=con.prepareStatement("select * from student where student_no=?");
             pst.setString(1, sid);
             rs=pst.executeQuery();
            if(rs.next()==true)
            {
                studno.setText(rs.getString(1));
                name.setText(rs.getString(2));
                dob.setText(rs.getString(3));
                doj.setText(rs.getString(4));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Perticular_Record.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

