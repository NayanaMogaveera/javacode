    public void loadid()
    {
        try {
            pst=con.prepareStatement("select student_no from student");
            rs = pst.executeQuery();
            studid.removeAllItems();
            while(rs.next())
            {
                studid.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Perticular_Record.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }
