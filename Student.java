    public void loadstudid()
    {
        try {
            pst=con.prepareStatement("select student_no from student");
            rs = pst.executeQuery();
            Student_id.removeAllItems();
            while(rs.next())
            {
                Student_id.addItem(rs.getString(1));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    

