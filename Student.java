    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
            String sno=stud_number.getText();
            String sname=stud_name.getText();
            String DOB=date_of_birth.getText();
            String DOJ=date_of_join.getText();
            pst= con.prepareStatement("insert into student values(?,?,?,?)");
            pst.setString(1,sno);
            pst.setString(2,sname);
            pst.setString(3,DOB);
            pst.setString(4,DOJ);
            
            int k = pst.executeUpdate();
            if(k==1)
            {
                JOptionPane.showMessageDialog(this,"Record Inserted");
                stud_number.setText("");
                stud_name.setText("");
                date_of_birth.setText("");
                date_of_join.setText("");
                stud_number.requestFocus();
                loadstudid();
                
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Record not Inserted");
            }
           
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
            
            
            
            
            
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String sid = Student_id.getSelectedItem().toString();
        try {
            pst=con.prepareStatement("select * from student where student_no=?");
            pst.setString(1, sid);
            rs=pst.executeQuery();
            if(rs.next()==true)
            {
                stud_number.setText(rs.getString(1));
                stud_name.setText(rs.getString(2));
                date_of_birth.setText(rs.getString(3));
                date_of_join.setText(rs.getString(4));
            }
           
                    
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

